#ifndef __MYIIC_H
#define __MYIIC_H
#include "sys.h"

//IO方向设置
 
#define SDA_IN()  {GPIOB->CRL&=0X0FFFFFFF;GPIOB->CRL|=(u32)8<<28;} //PB的所有引脚上拉输入
#define SDA_OUT() {GPIOB->CRL&=0X0FFFFFFF;GPIOB->CRL|=(u32)3<<28;} //PB的所有引脚推挽输出

//IO操作函数	 
#define IIC_SCL    PBout(6) //SCL
#define IIC_SDA    PBout(7) //SDA	 
#define READ_SDA   PBin(7)  //输入SDA 

//IIC所有操作函数
void IIC_Init_B30(void);                //初始化IIC的IO口				 
void IIC_Start_B30(void);				//发送IIC开始信号
void IIC_Stop_B30(void);	  			//发送IIC停止信号
void IIC_Send_Byte_B30(u8 txd);			//IIC发送一个字节
u8 IIC_Read_Byte_B30(unsigned char ack);//IIC读取一个字节
u8 IIC_Wait_Ack_B30(void); 				//IIC等待ACK信号
void IIC_Ack_B30(void);					//IIC发送ACK信号
void IIC_NAck_B30(void);				//IIC不发送ACK信号

void IIC_Write_One_Byte(u8 daddr,u8 addr,u8 data);
u8 IIC_Read_One_Byte(u8 daddr,u8 addr);	  
#endif
















